class Product {
  final String name;
  final String category;
  final double price;
  final String expiredDate;
  final String barcode;
  final String soldBy;
  final String unit;

  Product({
    required this.name,
    required this.category,
    required this.price,
    required this.expiredDate,
    required this.barcode,
    required this.soldBy,
    required this.unit,
  });
}
